CALL GET_COMMAND_ARGUMENT(1, ARG)
READ(ARG, *) NLAT
CALL GET_COMMAND_ARGUMENT(2, ARG)
READ(ARG, *) NLEV
CALL GET_COMMAND_ARGUMENT(3, ARG)
READ(ARG, *) ZN
CALL GET_COMMAND_ARGUMENT(4, ARG)
READ(ARG, *) ZB
CALL GET_COMMAND_ARGUMENT(5, ARG)
READ(ARG, *) ZRH0
CALL GET_COMMAND_ARGUMENT(6, ARG)
READ(ARG, *) ZT0
